package com.example.traceeye.data;

public class DataObject {
    public int eyeX;
    public int eyeY;
    public int targetX;
    public int targetY;

    public DataObject(int eyeX, int eyeY, int targetX, int targetY) {
        this.eyeX = eyeX;
        this.eyeY = eyeY;
        this.targetX = targetX;
        this.targetY = targetY;
    }
}
